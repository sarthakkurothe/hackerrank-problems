/*Question-1

Read an integer from keyboard using scanf and display it using printf.

Input Format

10

Constraints

Input should be a whole number.

Output Format

Number is 10

Sample Input 0

5
Sample Output 0

Number is 5

Solution-*/

#include<stdio.h>
int main()
{
    int x;
    scanf("%d",&x);
    printf("Number is %d\n",x);
    return 0;
    
}

