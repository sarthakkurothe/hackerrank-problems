/*Question-

Write a program in C in which given two integers N1 and N2, find their sum and store it as integer R.

Input Format

10 20

Constraints

All input numbers are integers.

Output Format

30

Sample Input 0

10
20
Sample Output 0

sum of numbers is 30

Solution-*/

#include<stdio.h>
int main()
{
    int a,b;
    scanf("%d",&a);
    scanf("%d",&b);
    printf("Sum of numbers is %d",a+b);
    return 0;
    
}